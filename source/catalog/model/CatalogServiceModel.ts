import {
    autoGeneratedHashKey,
    attribute,
    rangeKey,
    table,
} from '@aws/dynamodb-data-mapper-annotations';

@table('ServiceCatalog')
export default class CatalogServiceModel {
    @autoGeneratedHashKey()
    ServiceID: string;

    @rangeKey({defaultProvider: () => new Date()})
    CreatedAt: Date;

    @attribute()
    ServiceName: string;

    @attribute()
    StageName: string;

    @attribute()
    ServiceURL: string;
}
